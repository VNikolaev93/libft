#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *xs1;

	xs1 = s1;
	while (*xs1 != '\0')
		xs1++;
	while (*s2 != '\0')
		*xs1++ = *s2++;
	*xs1 = '\0';
	return (s1);
}
